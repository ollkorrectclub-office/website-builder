const projectsGrid = document.getElementById("projects-grid");
const signalStack = document.getElementById("signal-stack");
const checklist = document.getElementById("launch-checklist");
const templateGrid = document.getElementById("template-grid");
const channelChart = document.getElementById("channel-chart");
const activityFeed = document.getElementById("activity-feed");
const promptInput = document.getElementById("prompt-input");
const promptChips = document.getElementById("prompt-chips");
const marketButtons = document.querySelectorAll(".market-button");

let activeMarket = "al";

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#39;");
}

function renderProjects(projects) {
  projectsGrid.innerHTML = projects
    .map(
      (project) => `
        <article class="project-card">
          <div class="card-topline">
            <span class="project-status">${escapeHtml(project.status)}</span>
            <span class="project-city">${escapeHtml(project.city)}</span>
          </div>
          <h3>${escapeHtml(project.title)}</h3>
          <p>${escapeHtml(project.description)}</p>
          <div class="metric-row">
            ${project.metrics
              .map((metric) => `<span class="metric-pill">${escapeHtml(metric)}</span>`)
              .join("")}
          </div>
        </article>
      `
    )
    .join("");
}

function renderSignals(signals) {
  signalStack.innerHTML = signals
    .map(
      (signal) => `
        <article class="signal">
          <span class="signal-tag">${escapeHtml(signal.tag)}</span>
          <h3>${escapeHtml(signal.title)}</h3>
          <p>${escapeHtml(signal.copy)}</p>
        </article>
      `
    )
    .join("");
}

function renderChecklist(items) {
  checklist.innerHTML = items.map((item) => `<li>${escapeHtml(item)}</li>`).join("");
}

function renderTemplates(templates) {
  templateGrid.innerHTML = templates
    .map(
      (template) => `
        <article class="template-card">
          <span class="template-tag">${escapeHtml(template.tag)}</span>
          <h3>${escapeHtml(template.title)}</h3>
          <p>${escapeHtml(template.copy)}</p>
        </article>
      `
    )
    .join("");
}

function renderChannels(channels) {
  channelChart.innerHTML = channels
    .map(
      (channel) => `
        <article class="chart-bar">
          <div class="chart-label">
            <span>${escapeHtml(channel.label)}</span>
            <span>${escapeHtml(channel.value)}%</span>
          </div>
          <div class="chart-track">
            <div class="chart-fill" style="width: ${channel.value}%"></div>
          </div>
        </article>
      `
    )
    .join("");
}

function renderActivity(items) {
  activityFeed.innerHTML = items
    .map(
      (item) => `
        <article class="activity-item">
          <strong>${escapeHtml(item.title)}</strong>
          <p>${escapeHtml(item.copy)}</p>
          <span class="activity-time">${escapeHtml(item.time)}</span>
        </article>
      `
    )
    .join("");
}

function renderChips(chips) {
  promptChips.innerHTML = chips
    .map((chip) => `<button class="chip" type="button">${escapeHtml(chip)}</button>`)
    .join("");

  promptChips.querySelectorAll(".chip").forEach((chipButton) => {
    chipButton.addEventListener("click", () => {
      promptInput.value = `Krijo një website modern për ${chipButton.textContent.toLowerCase()} me fokus te konvertimet, shpejtësia dhe një CTA të qartë.`;
    });
  });
}

function setText(id, value) {
  document.getElementById(id).textContent = value;
}

function renderMarket(marketKey) {
  activeMarket = marketKey;
  const data = marketData[marketKey];

  document.body.dataset.market = marketKey;
  marketButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.market === marketKey);
  });

  setText("sidebar-market-title", data.label);
  setText("sidebar-market-copy", data.sidebarCopy);
  setText("sidebar-leads", data.leads);
  setText("sidebar-build-time", data.buildTime);
  setText("hero-highlight-market", data.heroMarket);
  setText("hero-highlight-speed", data.heroSpeed);
  setText("composer-market-badge", data.badge);
  setText("prompt-meta", data.meta);
  setText("stat-pipeline", data.leads);
  setText("stat-pipeline-copy", data.pipelineCopy);
  setText("stat-build", data.buildValue);
  setText("stat-payment", data.payment);
  setText("stat-sector", data.sector);
  setText("stat-sector-copy", data.sectorCopy);
  promptInput.value = data.prompt;

  renderProjects(data.projects);
  renderSignals(data.signals);
  renderChecklist(data.checklist);
  renderTemplates(data.templates);
  renderChannels(data.channels);
  renderActivity(data.activity);
  renderChips(data.chips);
}

function createProjectFromPrompt() {
  const prompt = promptInput.value.trim();
  if (!prompt) {
    promptInput.focus();
    return;
  }

  const city = activeMarket === "al" ? "Tiranë" : "Prishtinë";
  const status = activeMarket === "al" ? "Fresh concept" : "Launch ready";
  const titleSeed = prompt
    .replace(/^Krijo\s+nj[eë]?\s+/i, "")
    .split(" ")
    .slice(0, 4)
    .join(" ");
  const title = titleSeed.charAt(0).toUpperCase() + titleSeed.slice(1);

  const project = {
    title,
    city,
    status,
    description: prompt,
    metrics:
      activeMarket === "al"
        ? ["CTA lokale", "Mobile-first", "WhatsApp"]
        : ["Trust-first", "EUR", "Lead form"],
  };

  marketData[activeMarket].projects.unshift(project);
  marketData[activeMarket].activity.unshift({
    title: `${title} u shtua si koncept i ri`,
    copy:
      activeMarket === "al"
        ? "U krijua një draft i ri me fokus te CTA lokale dhe ritmi i shpejtë i launch-it."
        : "U krijua një draft i ri me trust blocks, paketat në EUR dhe lead capture të thjeshtë.",
    time: "tani",
  });

  renderProjects(marketData[activeMarket].projects);
  renderActivity(marketData[activeMarket].activity.slice(0, 3));
}

document.getElementById("generate-project").addEventListener("click", createProjectFromPrompt);
document.getElementById("generate-brief").addEventListener("click", createProjectFromPrompt);

marketButtons.forEach((button) => {
  button.addEventListener("click", () => {
    renderMarket(button.dataset.market);
  });
});

renderMarket(activeMarket);
